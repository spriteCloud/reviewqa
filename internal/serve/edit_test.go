const editableFeature = `# Generated by quail.

Feature: Example — read journey

  @journey:read @smoke
  Scenario: lands on the terminal page
    Given I open the landing page
    Then I see the heading "About"

  @journey:read @kind:resume
  Scenario: deep-link to the terminal page
    Given I open the page "/about"
    Then I see the heading "About"
`

func writeFixture(t *testing.T, body string) (root, featurePath string) {
	t.Helper()
	root = t.TempDir()
	featurePath = filepath.Join(root, "tests", "e2e", "features", "demo.feature")
	mustWrite(t, featurePath, body)
	return
}

func TestFindScenarioRange_TagBlockIncluded(t *testing.T) {
	lines := strings.Split(editableFeature, "\n")
	rng, ok := findScenarioRange(lines, "lands on the terminal page")
	if !ok {
		t.Fatal("scenario not found")
	}
	// The @journey:read @smoke line must be the start.
	if !strings.HasPrefix(strings.TrimSpace(lines[rng.Start]), "@") {
		t.Errorf("expected range to start at the @tag line; got %q", lines[rng.Start])
	}
}

func TestDeleteScenario_RemovesBlockAndBacksUp(t *testing.T) {
	root, path := writeFixture(t, editableFeature)
	history := filepath.Join(root, "tests", "e2e", ".quail-history")
	n, err := DeleteScenario(path, "lands on the terminal page", history)
	if err != nil {
		t.Fatalf("delete: %v", err)
	}
	if n != 1 {
		t.Errorf("expected 1 deleted, got %d", n)
	}
	b, _ := os.ReadFile(path)
	if strings.Contains(string(b), "lands on the terminal page") {
		t.Errorf("scenario name still present after delete:\n%s", b)
	}
	if !strings.Contains(string(b), "deep-link to the terminal page") {
		t.Errorf("the other scenario was removed unintentionally")
	}
	// Backup must exist.
	backupPath := filepath.Join(history, "20260618-000000", "demo.feature")
	if _, err := os.Stat(backupPath); err != nil {
		t.Errorf("expected backup at %s, got err %v", backupPath, err)
	}
}

