func TestHandler_ScenarioAppend(t *testing.T) {
	root := fixtureProject(t)
	srv := httptest.NewServer(Handler(root))
	defer srv.Close()
	newBlock := "Scenario: appended\n  Given I open the landing page\n"
	body, _ := json.Marshal(map[string]string{"gherkin": newBlock})
	resp, err := http.Post(srv.URL+"/api/scenario?feature=tests/e2e/features/demo.feature", "application/json", bytes.NewReader(body))
	if err != nil {
		t.Fatal(err)
	}
	defer resp.Body.Close()
	if resp.StatusCode != 200 {
		buf := make([]byte, 1024)
		n, _ := resp.Body.Read(buf)
		t.Fatalf("status: %d body=%s", resp.StatusCode, buf[:n])
	}
	// Re-fetch and confirm the appended Scenario lives.
	got, err := http.Get(srv.URL + "/api/feature?path=tests/e2e/features/demo.feature")
	if err != nil {
		t.Fatal(err)
	}
	defer got.Body.Close()
	var pulled struct {
		Feature Feature `json:"feature"`
	}
	json.NewDecoder(got.Body).Decode(&pulled)
	found := false
	for _, s := range pulled.Feature.Scenarios {
		if s.Name == "appended" {
			found = true
			break
		}
	}
	if !found {
		t.Errorf("appended scenario not visible after refetch: %+v", pulled.Feature.Scenarios)
	}
}
