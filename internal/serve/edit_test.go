func TestAppendScenario_AddsAtEnd(t *testing.T) {
	root, path := writeFixture(t, editableFeature)
	history := filepath.Join(root, ".history")
	newBlock := `  @new
  Scenario: brand new path
    Given I open the landing page
    Then I see the heading "Welcome"`
	name, err := AppendScenario(path, newBlock, history)
	if err != nil {
		t.Fatalf("append: %v", err)
	}
	if name != "brand new path" {
		t.Errorf("name: got %q", name)
	}
	b, _ := os.ReadFile(path)
	src := string(b)
	if !strings.Contains(src, "brand new path") {
		t.Errorf("new scenario missing:\n%s", src)
	}
	if !strings.HasSuffix(strings.TrimRight(src, "\n"), `Then I see the heading "Welcome"`) {
		t.Errorf("expected new scenario at EOF; got tail:\n%s", src[len(src)-200:])
	}
}

