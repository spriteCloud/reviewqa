func TestReplaceScenario_KeepsRestUntouched(t *testing.T) {
	root, path := writeFixture(t, editableFeature)
	history := filepath.Join(root, "tests", "e2e", ".quail-history")
	newBlock := `  @journey:read @smoke @edited
  Scenario: lands on the terminal page (renamed)
    Given I open the landing page
    When I navigate directly to "/about"
    Then I see the heading "About"`
	newName, err := ReplaceScenario(path, "lands on the terminal page", newBlock, history)
	if err != nil {
		t.Fatalf("replace: %v", err)
	}
	if newName != "lands on the terminal page (renamed)" {
		t.Errorf("rename: got %q", newName)
	}
	b, _ := os.ReadFile(path)
	src := string(b)
	if !strings.Contains(src, "lands on the terminal page (renamed)") {
		t.Errorf("renamed scenario not in output:\n%s", src)
	}
	if strings.Contains(src, "lands on the terminal page\n    Given I open the landing page\n    Then I see the heading \"About\"\n") {
		t.Errorf("old scenario block still present:\n%s", src)
	}
	if !strings.Contains(src, "deep-link to the terminal page") {
		t.Errorf("other scenario lost:\n%s", src)
	}
}

